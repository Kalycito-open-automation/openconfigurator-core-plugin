package org.epsg.openconfigurator.editors.project.xml;

import org.eclipse.jface.text.rules.IPredicateRule;
import org.eclipse.jface.text.rules.IToken;
import org.eclipse.jface.text.rules.MultiLineRule;
import org.eclipse.jface.text.rules.RuleBasedPartitionScanner;
import org.eclipse.jface.text.rules.Token;

/**
 * XML scanner that exclusively uses predicate rules.
 *
 * @author Ramakrishnan P
 *
 */
public class XmlPartitionScanner extends RuleBasedPartitionScanner {
    /**
     * XML comment marker
     */
    public final static String XML_COMMENT = "__xml_comment";

    /**
     * XML tag marker
     */
    public final static String XML_TAG = "__xml_tag";

    /**
     * Constructor
     */
    public XmlPartitionScanner() {

        IToken xmlComment = new Token(XmlPartitionScanner.XML_COMMENT);
        IToken tag = new Token(XmlPartitionScanner.XML_TAG);

        IPredicateRule[] rules = new IPredicateRule[2];

        rules[0] = new MultiLineRule("<!--", "-->", xmlComment);
        rules[1] = new TagRule(tag);

        setPredicateRules(rules);
    }
}
